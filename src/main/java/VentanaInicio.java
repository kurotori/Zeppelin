    private void btLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btLoginMouseClicked
        InfoDrive info = new InfoDrive();
        
        if(!loginOk){
            try {
                String cuenta=info.NombreCuenta();
                String capacidad = info.CapacidadCuenta();
                btLogin.setText(cuenta);
                loginOk=true;
                texto1.append("\nConectado a "+cuenta);
                texto1.append("\nAlmacenamiento: "+capacidad+"\n");
            } catch (IOException ex) {
                texto1.append(ex.getMessage());
                //Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
            } catch (GeneralSecurityException ex) {
                texto1.append(ex.getMessage());
                //Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            boolean salirOk=false;
            Alerta alerta = new Alerta(this, true);
            alerta.mensaje="¿Cerrar Sesión?";
            alerta.setVisible(true);
            //salirOk=alerta.getReturnStatus();
            
            if(alerta.getReturnStatus()==1){
               Archivos arch = new Archivos();
                arch.ChequearDirCredenciales(texto1);
                btLogin.setText("Iniciar Sesión");
                loginOk=false; 
            }
            
        }

        
        
    }//GEN-LAST:event_btLoginMouseClicked

