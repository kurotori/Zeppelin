    private void btVerDirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btVerDirMouseClicked
        
        Archivos arch = new Archivos();
        JFileChooser selector = new JFileChooser();
        selector.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        selector.setAcceptAllFileFilterUsed(false);
        int returnVal = selector.showOpenDialog(btVerDir);
        if(returnVal == JFileChooser.APPROVE_OPTION) {
            File directorio=selector.getSelectedFile();
            arch.VerArchivos(directorio, texto1);
        }
    }//GEN-LAST:event_btVerDirMouseClicked

    private void btSubirArchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btSubirArchMouseClicked
        Archivos arch = new Archivos();
        JFileChooser selector = new JFileChooser();
        selector.setFileSelectionMode(JFileChooser.FILES_ONLY);
        selector.setAcceptAllFileFilterUsed(false);
        int returnVal = selector.showOpenDialog(btSubirArch);
        if(returnVal == JFileChooser.APPROVE_OPTION) {
            File directorio=selector.getSelectedFile();
            arch.SubirArchivo(directorio,texto1);
        }
    }//GEN-LAST:event_btSubirArchMouseClicked

